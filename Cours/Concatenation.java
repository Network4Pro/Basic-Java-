package Cours;
public class Concatenation {
    public static void main(String[] args) {
        String Chaine1 = "Youssef";
        String Chaine2 = " El khanchoufi";
        System.out.println(Chaine1);
        System.out.println(Chaine2);

        System.out.println( Chaine1 + Chaine2);

        String NB1 = "20";
        String NB2 = "17";
        System.out.println(NB1 + NB2);

        int a = 100;
        System.out.println(a + NB1);

        boolean bol = true;
        System.out.println(NB1 + bol + a);

        System.out.println("\'Youssef\'");
        System.out.println("\"El khanchoufi\"");
        System.out.println("\\TDI - TRI\\");

    }
}
